package sept.ex_16092024;

public class Lab029_Relational_Operator {
    public static void main(String[] args) {
        // RElational operators always return true or false value
        // >,<,!=,>=,<=
        int a=10;
        int b=20;
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a!=b);




    }
}
