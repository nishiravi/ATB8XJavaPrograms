package sept.ex_16092024;

public class Lab28_Compound_Assignment_Operator {
    public static void main(String[] args) {
        // Compound Assignment operator
        // +=,-=,*=,/=
        int a=10;
        a+=20; // a=a+20;
        System.out.println(a);
        a-=2; // a=a-2;
        System.out.println(a);
        a*=2;//a=a*2;
        System.out.println(a);
        a/=2;//a=a/2;
        System.out.println(a);
    }
}
