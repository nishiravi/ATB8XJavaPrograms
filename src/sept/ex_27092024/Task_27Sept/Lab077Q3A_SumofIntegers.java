package sept.ex_27092024.Task_27Sept;

public class Lab077Q3A_SumofIntegers {
    public static void main(String[] args) {
        int i=1;
        int sum=0;
        while(i<=100)
        {
            sum=sum+i;
            i++;
        }
        System.out.println("  sum of numbers from 1 to 100 is  "+sum);
    }
}
