public class Lab022_Constants {
    public static void main(String[] args) {
        //Constant- are the variable values that cannot be changed once assigned
        // we use final keyword with the data_type to declare  a variable as constant
        //Syntax: final DATA_TYPE variable name=value;
        final float pi=3.147f;
          // Final float pi; // statement valid
        // pi=3.147;// statement valid - because pi is assigned value for the first time
        //pi=3.147; this statement will throw error-because pi value is already assigned

    }
}
