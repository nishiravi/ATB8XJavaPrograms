package sept.ex_18092024;

public class Lab040_PostIncrement_Operator {
    public static void main(String[] args) {

        // Post increment-first print then increment;
        int a2=12;
        int a3=a2++;
        System.out.println(a2);
        System.out.println(a3); // just print the value before increment
        System.out.println(a3+a2);// here a3 value will be incremented








    }
}
